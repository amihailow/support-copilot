import "dotenv/config";
import fs from "node:fs";
import path from "node:path";
import yaml from "js-yaml";
import { runPipeline } from "@/lib/pipeline";
import { detectMode } from "@/lib/mode";
import {
  aggregate,
  metricStatus,
  scoreCase,
  type AggregateMetrics,
  type CaseResult,
  type ExpectedFields,
  type MetricDef,
} from "@/lib/eval-scoring";

interface TestCase {
  id: string;
  ticket_id: string;
  ticket: {
    subject: string;
    body: string;
  };
  expected: ExpectedFields;
}

interface Dataset {
  tests: TestCase[];
  metrics: MetricDef[];
}

function loadDataset(): Dataset {
  const file = path.resolve(process.cwd(), "evals/dataset.yaml");
  return yaml.load(fs.readFileSync(file, "utf8")) as Dataset;
}

async function evalCase(tc: TestCase): Promise<CaseResult> {
  const result = await runPipeline({
    ticketId: tc.ticket_id,
    subject: tc.ticket.subject,
    body: tc.ticket.body,
  });
  return scoreCase(tc.id, tc.expected, result);
}

function renderConsole(
  results: CaseResult[],
  agg: AggregateMetrics,
  dataset: Dataset,
  mode: string,
) {
  console.log(`\nMode: ${mode}\n`);
  console.log("Case-by-case:");
  console.log(
    "  " + "id".padEnd(28) + "cat  pri  cite phrase  bad  pass",
  );
  for (const r of results) {
    const row =
      r.id.padEnd(28) +
      (r.category_correct ? "ok " : "ERR ").padEnd(5) +
      (r.priority_correct ? "ok " : "ERR ").padEnd(5) +
      (r.citation_recall ? "ok " : "ERR ").padEnd(5) +
      (r.required_phrase_hit ? "ok    " : "ERR   ").padEnd(8) +
      `${r.prohibited_phrase_hits}`.padEnd(5) +
      (r.pass ? "PASS" : "FAIL");
    console.log("  " + row);
    for (const note of r.notes) console.log("    - " + note);
  }

  console.log("\nAggregate metrics:");
  let failed = 0;
  for (const metric of dataset.metrics) {
    const s = metricStatus(
      metric.name as keyof AggregateMetrics,
      agg,
      metric,
    );
    if (s.status === "fail") failed++;
    const flag = s.status === "pass" ? "[ok]  " : "[fail]";
    console.log(
      `  ${flag} ${metric.name.padEnd(32)} ${s.valueStr.padEnd(10)} target ${s.targetStr}`,
    );
  }

  console.log("");
  if (failed === 0) console.log("All targets met.");
  else console.log(`${failed} target(s) missed.`);
  return failed;
}

function renderMarkdown(
  results: CaseResult[],
  agg: AggregateMetrics,
  dataset: Dataset,
  mode: string,
): string {
  const lines: string[] = [];
  const now = new Date().toISOString();

  lines.push("# Eval Results", "");
  lines.push("Generated by `npm run eval`. This file is overwritten on every run.", "");
  lines.push(`- Mode: \`${mode}\``);
  lines.push(`- Generated: ${now}`);
  lines.push(`- Dataset: ${dataset.tests.length} test case(s)`, "");

  lines.push("## Aggregate metrics", "");
  lines.push("| Metric | Value | Target | Status |");
  lines.push("| --- | --- | --- | --- |");
  for (const metric of dataset.metrics) {
    const s = metricStatus(
      metric.name as keyof AggregateMetrics,
      agg,
      metric,
    );
    lines.push(
      `| ${metric.name} | ${s.valueStr} | ${s.targetStr} | ${s.status === "pass" ? "pass" : "**fail**"} |`,
    );
  }
  lines.push("");

  lines.push("## Case-by-case", "");
  lines.push(
    "| ID | Category | Priority | Citation | Required phrase | Prohibited | Latency | Cost | Pass |",
  );
  lines.push("| --- | --- | --- | --- | --- | --- | --- | --- | --- |");
  for (const r of results) {
    lines.push(
      `| ${r.id} | ${r.category_correct ? "ok" : "**fail**"} | ${r.priority_correct ? "ok" : "**fail**"} | ${r.citation_recall ? "ok" : "**fail**"} | ${r.required_phrase_hit ? "ok" : "**fail**"} | ${r.prohibited_phrase_hits} | ${r.latency_ms.toFixed(0)}ms | $${r.cost_usd.toFixed(4)} | ${r.pass ? "pass" : "**fail**"} |`,
    );
  }
  lines.push("");

  if (results.some((r) => r.notes.length > 0)) {
    lines.push("## Failures", "");
    for (const r of results) {
      if (r.notes.length === 0) continue;
      lines.push(`### ${r.id}`);
      for (const n of r.notes) lines.push(`- ${n}`);
      lines.push("");
    }
  }

  lines.push("## Methodology", "");
  for (const m of dataset.metrics) {
    lines.push(`- **${m.name}** - ${m.description}`);
  }
  lines.push("");
  lines.push(
    "In live mode, faithfulness is scored by an LLM judge against",
    "the retrieved chunks. In mock mode it falls back to a proxy:",
    "fraction of drafts that include at least one `[doc-N]` citation",
    "when retrievals are available.",
    "",
  );

  return lines.join("\n");
}

async function main() {
  const dataset = loadDataset();
  const { mode, missingKeys } = detectMode();

  console.log(`Running eval suite (${dataset.tests.length} case(s))...`);
  if (mode === "mock" && missingKeys.length > 0) {
    console.log(`(mock mode - missing: ${missingKeys.join(", ")})`);
  }

  const results: CaseResult[] = [];
  for (const tc of dataset.tests) {
    process.stdout.write(`  ${tc.id.padEnd(28)} `);
    try {
      const r = await evalCase(tc);
      results.push(r);
      console.log(r.pass ? "PASS" : "FAIL");
    } catch (e) {
      console.log(`ERROR: ${e instanceof Error ? e.message : e}`);
      throw e;
    }
  }

  const agg = aggregate(results);
  const failed = renderConsole(results, agg, dataset, mode);

  const md = renderMarkdown(results, agg, dataset, mode);
  fs.writeFileSync(path.resolve(process.cwd(), "evals/RESULTS.md"), md);

  const json = {
    generatedAt: new Date().toISOString(),
    mode,
    dataset: {
      count: dataset.tests.length,
      metrics: dataset.metrics,
    },
    aggregate: agg,
    cases: results,
    metricStatus: Object.fromEntries(
      dataset.metrics.map((m) => [
        m.name,
        metricStatus(m.name as keyof AggregateMetrics, agg, m),
      ]),
    ),
  };
  fs.writeFileSync(
    path.resolve(process.cwd(), "evals/results.json"),
    JSON.stringify(json, null, 2),
  );

  console.log(`\nWritten: evals/RESULTS.md and evals/results.json`);
  process.exit(failed === 0 ? 0 : 1);
}

main().catch((e) => {
  console.error("\nEval failed:", e instanceof Error ? e.message : e);
  process.exit(2);
});
